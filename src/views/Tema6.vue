    p(data-aos="fade-right") Para García, Romero &amp; Canales (2010), la existencia del “método de Optimización multi-objetivo por análisis de radio proporción (MOORA, por sus siglas en inglés), el cual fue introducido por primera vez en el 2006 por Brauers y Zavadskas, genera otra perspectiva en el proceso de evaluación de proveedores”. La idea básica de este proceso es calcular el ingreso total de cada opción como la diferencia entre los beneficios totales normalizados según los criterios de coste y beneficio. Antes de comenzar, es importante tener en cuenta que todos los atributos están bien definidos y que todos estos atributos deben medirse o evaluarse en función de cada opción.
    p(data-aos="fade-right") Las métricas son aspectos de la calidad del servicio, el cumplimiento de los plazos y la gestión. Estos son criterios importantes para el desempeño de los proveedores en cada grupo de proveedores de servicios. Estas columnas o atributos deben ponderarse de acuerdo con las necesidades comerciales, por lo que cada columna o atributo se multiplica por su respectiva ponderación o importancia para obtener una matriz de valores de ponderación normalizados.

